export const getMatchingMedicines = (gptResults) => {
  if (!gptResults || !Array.isArray(gptResults)) return [];

  const matches = [];

  gptResults.forEach((result) => {
    const resultLower = result.toLowerCase();
    const match = medicineInventory.find(
      (med) =>
        med.name.toLowerCase().includes(resultLower) ||
        resultLower.includes(med.name.toLowerCase()) ||
        med.category.toLowerCase().includes(resultLower) ||
        med.ingredients.toLowerCase().includes(resultLower)
    );
    if (match && !matches.find((m) => m.id === match.id)) {
      matches.push(match);
    }
  });

  return matches;
};

export const findMedicineByName = (medicineName) => {
  if (!medicineName) return null;

  const nameLower = medicineName.toLowerCase();
  return medicineInventory.find(
    (med) =>
      med.name.toLowerCase().includes(nameLower) ||
      nameLower.includes(med.name.toLowerCase())
  );
};

export const getMedicinesByCategory = (category) => {
  if (!category) return [];

  const categoryLower = category.toLowerCase();
  return medicineInventory.filter((med) =>
    med.category.toLowerCase().includes(categoryLower)
  );
};

